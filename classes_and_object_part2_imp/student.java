package classes_and_object_part2_imp;

public class student {
    int id;
    String name;
    int age;

    public void setid(int id)
    {
        this.id = id;
    }
    public int getid()
    {
        return this.id;
    }

    public void setname(String name)
    {
        this.name = name;
    }
    public String getname()
    {
        return this.name;
    }

    public void setage(int age)
    {
        this.age = age;
    }
    public int getage()
    {
        return this.age;
    }

}
