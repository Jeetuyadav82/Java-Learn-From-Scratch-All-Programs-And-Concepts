public class switch_statement_in_java {
    public static void main(String args[])
    {
        int score = 60;
        switch(score)
        {
            case 55 :

            case 50 :
            {
                System.out.println("Very good");
                break;

            }

            case 60 :
            {
                System.out.println("average");
                break;

            }

            default :
            {
                System.out.println("Not defined");
                break;

            }
        }
    }
}
